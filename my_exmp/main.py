# Создаем список
my_list = [1, 2, 3, 3, 4, 5]

# Преобразуем список в кортеж
my_tuple = tuple(my_list)  # (1, 2, 3, 3, 4, 5)

# Преобразуем список во множество (дубликаты будут удалены)
my_set = set(my_list)  # {1, 2, 3, 4, 5}

# Создаем словарь из списка пар
